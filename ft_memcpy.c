#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	size_t	i;
	char	*dst;
	char	*src;

	i = 0;
	dst = s1;
	src = (char *)s2;
	while (i < n)
	{
		*(dst + i) = *(src + i);
		i++;
	}
	return (s1);
}
