#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	len;

	len = n;
	i = 0;
	while (*(s2 + i) && n > 0)
	{
		*(s1 + i) = *(s2 + i);
		n--;
		i++;
	}
	if (ft_strlen(s2) < len)
	{
		while (*(s1 + i) && n > 0)
		{
			*(s1 + i) = '\0';
			i++;
			n--;
		}
	}
	return (s1);
}
