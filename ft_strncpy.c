#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	while (s2 && *s2 && n > 0)
	{
		*s1 = *s2;
		s1++;
		s2++;
		n--;
	}
	return (s1);
}
