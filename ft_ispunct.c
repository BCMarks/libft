#include "libft.h"

int	ft_ispunct(int n)
{
	if ((n >= 33 && n <= 47) || (n >= 58 & n <= 64) ||
			(n >= 91 && n <= 96) || (n >= 123 && n <= 126))
		return (1);
	else
		return (0);
}
